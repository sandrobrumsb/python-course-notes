"""
Operadores Lógicos Python:
and 	Returns True if both statements are true	x < 5 and  x < 10	
or	Returns True if one of the statements is true	x < 5 or x < 4	
not	Reverse the result, returns False if the result is true	not(x < 5 and x < 10)
"""
x=10
print(x > 3 or x < 4)

"""
Operadores de identidade Python:
is 	Returns True if both variables are the same object	x is y	
is not	Returns True if both variables are not the same object	x is not y:
"""
x = ["apple", "banana"]
y = ["apple", "banana"]
z = x

print(x is z) # returns True because z is the same object as x
print(x is y) # returns False because x is not the same object as y, even if they have the same content
print(x == y) # to demonstrate the difference betweeen "is" and "==": this comparison returns True because x is equal to y

"""
Operadores de Associação Python:
Operadores de associação são usados para testar se uma sequência é apresentada em um objeto:
"""
x = ["apple", "banana"]
print("banana" in x)

y = ["apple", "banana"]
print("pineapple" not in y)


"""
Operadores binários:

& 	AND	Sets each bit to 1 if both bits are 1	x & y	
|	OR	Sets each bit to 1 if one of two bits is 1	x | y	
^	XOR	Sets each bit to 1 if only one of two bits is 1	x ^ y	
~	NOT	Inverts all the bits	~x	
<<	Zero fill left shift	Shift left by pushing zeros in from the right and let the leftmost bits fall off	x << 2	
>>	Signed right shift	Shift right by pushing copies of the leftmost bit in from the left, and let the rightmost bits fall off	x >> 2
"""
print(6 ^ 3)


"""
Parênteses têm a maior precedência, 
 o que significa que as expressões dentro deles devem ser avaliadas primeiro:
"""
print((6 + 3) - (6 + 3))

"""
A multiplicação *tem precedência maior que a adição +e,
portanto, as multiplicações são avaliadas antes das adições:
"""
print(100 + 5 * 3)

